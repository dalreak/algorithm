starShape = [["*"]]
num = int(input())
n = 1
while (3 ** n) != num:
    n += 1
for i in range(1,n+1):
    temp = list()
    for j in range(3):
        if j == 0 and i != 0:
            for k in range(len(starShape[i - 1])):
                temp.append(starShape[i-1][k] * 3)
        elif j == 1 and i != 0:
            for k in range(len(starShape[i - 1])):
                temp.append(starShape[i-1][k] + " " * len(starShape[i-1][k]) +starShape[i-1][k])
        elif j == 2 and i != 0:
            for k in range(len(starShape[i-1])):
                temp.append(starShape[i-1][k] * 3)
    starShape.append(temp)
